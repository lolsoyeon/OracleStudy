--EMPLOYEES3 테이블
CREATE TABLE EMPLOYEES3
( EMPLOYEE_ID           NUMBER(6)
, FIRST_NAME            VARCHAR2(20)
, LAST_NAME             VARCHAR2(25)        CONSTRAINT EMP3_LAST_NAME_NN NOT NULL
, EMAIL                 VARCHAR2(25)        CONSTRAINT EMP3_EMAIL_NN NOT NULL
, PHONE_NUMBER          VARCHAR2(20)
, HIRE_DATE             DATE                CONSTRAINT EMP3_HIRE_DATE_NN NOT NULL
, JOB_ID                VARCHAR2(10)        CONSTRAINT EMP3_JOB_NN NOT NULL
, SALARY                NUMBER(8,2)
, COMMISSION_PCT        NUMBER(2,2)
, MANAGER_ID            NUMBER(6)
, DEPARTMENT_ID         NUMBER(4)
, CONSTRAINT EMP3_EMP_ID_PK PRIMARY KEY(EMPLOYEE_ID)        -- EMPLOYEE_ID를 PK로 지정 
, CONSTRAINT EMP3_EMAIL_UK UNIQUE(EMAIL)                    -- EMAIL을 U로 지정
, CONSTRAINT EMP3_SALARY_MIN CHECK(salary > 0)              -- SALARY 제약조건 salary > 0
, CONSTRAINT EMP3_JOB_FK FOREIGN KEY(JOB_ID) REFERENCES JOBS3 (JOB_ID)                          -- JOBS3 테이블에서 JOB_ID 참조
, CONSTRAINT EMP3_MANAGER_FK FOREIGN KEY(MANAGER_ID) REFERENCES EMPLOYEES3 (EMPLOYEE_ID)         -- EMPLOYEES3 테이블에서 EMPLOYEE_ID 참조
, CONSTRAINT EMP3_DEPT_FK FOREIGN KEY(DEPARTMENT_ID) REFERENCES DEPARTMENTS3 (DEPARTMENT_ID)    -- DEPARTMENTS3 테이블에서 DEPARTMENT_ID 참조
);


