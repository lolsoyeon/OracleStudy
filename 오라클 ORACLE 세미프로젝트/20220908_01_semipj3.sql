-- 중도탈락 사유 테이블 생성
-- ⒫탈락사유코드, 중도탈락사유
CREATE TABLE QUIT_REASON
( QR_CODE 	    VARCHAR2(10)	
, QUIT_REASON	VARCHAR2(40)
, CONSTRAINT QUIT_REASON_PK PRIMARY KEY(QR_CODE)
);

-- 교수자 테이블
